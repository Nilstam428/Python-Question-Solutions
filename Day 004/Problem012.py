#Q12. how to overrides method ?
#A12. method overriding is a feature of OOPs in which a class provides a specific implementation of a method that is already provided by its superclass.
# The method in the subclass has the same name, return type and parameter list as the method in the superclass but it may have different implementation.
#Example 

# Method overriding is used when utlizing the same function for same parameters with different data types.

