# Q how to remove element from list
# methods
# 1. remove()
# 2. pop()

# program to demonstrate use of del keyword 

# assign list 
numbers = [1, 2, 3, 2, 3, 4, 5] 

# use remove 
# removes the first occurence
numbers.remove(3) 
print(numbers)

# print list
print(numbers)

# use pop
# removes the last occurence
numbers.pop()
print(numbers)
